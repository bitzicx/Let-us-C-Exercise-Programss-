// Write down macro definitions for the following:
// 1. To find arithmetic mean of two numbers.
// 2. To find absolute value of a number.
// 3. To convert a uppercase alphabet to lowercase.
// 4. To obtain the bigger of two numbers.


// Macro to find the arithmetic mean of two numbers
#define ARITHMETIC_MEAN(x, y) (((x) + (y)) / 2.0)

// Macro to find the absolute value of a number
#define ABSOLUTE_VALUE(x) ((x) < 0 ? -(x) : (x))

// Macro to convert an uppercase alphabet to lowercase
#define TO_LOWER_CASE(c) ((c) + 32)

// Macro to obtain the bigger of two numbers
#define MAX(x, y) ((x) > (y) ? (x) : (y)